    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
  String username = txtLogUsername.getText();
    String password = new String(txtLogPassword.getPassword());

    // Validate input fields
    if (username.isEmpty() || "Username".equals(username)) {
        JOptionPane.showMessageDialog(this, "Please enter a username.", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    if (password.isEmpty() || "Password".equals(password)) {
        JOptionPane.showMessageDialog(this, "Please enter a password.", "Warning", JOptionPane.WARNING_MESSAGE);
        return;
    }

    try {
        // SQL Query to validate credentials
        String query = "SELECT * FROM users WHERE accUsername = ? AND accPassword = ?";
        pst = con.prepareStatement(query);
        pst.setString(1, username);
        pst.setString(2, password);
        

        rs = pst.executeQuery();

        if (rs.next()) {
            // Login successful
                    String Username = rs.getString("Name");
            homepage homeage = null;
            // Redirect to the homepage or main application window
            homepage home = new homepage(homeage, Username);
            home.setVisible(true);
            this.dispose(); // Close the login window
        } else {
            // Login failed
            JOptionPane.showMessageDialog(this, "Invalid username or password.", "Error", JOptionPane.ERROR_MESSAGE);
        }

        // Close the result set and statement
        rs.close();
        pst.close();
    } catch (SQLException ex) {
        Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Database error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_loginActionPerformed

